# Working with Numbers in Python
# In Python, numbers are a fundamental data type that we use to perform various mathematical operations.
# In this section, we will explore the different types of numbers in Python and how to work with them effectively.

rounding_number = 3.14159
print(round(rounding_number, 2))  # Output: 3.14 (rounding to 2 decimal places)

absolute_value = -5
print(abs(absolute_value))  # Output: 5 (absolute value)

# You can also use the built-in math module for more advanced mathematical operations.
import math
print(math.sqrt(16))  # Output: 4.0 (square root)
print(math.pow(2, 3))  # Output: 8.0 (2 raised to the power of 3)           

